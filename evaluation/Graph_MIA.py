import torch
import torch.nn.functional as F
import numpy as np
from sklearn.metrics import roc_auc_score, accuracy_score, average_precision_score
import matplotlib.pyplot as plt
import seaborn as sns  # Use seaborn to draw a Figure-1-like density plot

class GraphMIA:
    """
    Loss-based membership inference attack (MIA) inspired by "White-box vs Black-box".

    Used to evaluate federated graph unlearning.
    Implements a MALT-style (loss-threshold) attack following Sablayrolles et al. (2019),
    with mask handling and metric tweaks for the federated graph unlearning setting.
    """
    def __init__(self, model, device='cuda'):
        self.model = model
        self.model.eval()
        self.model.to(device)
        self.device = device

    def check_data_integrity(self, data):
        """
        Ensure virtual graphs generated by VGAE and real graphs have compatible feature/structure fields.
        """
        assert hasattr(data, 'x'), "Data must contain node features 'x'"
        assert hasattr(data, 'edge_index'), "Data must contain graph structure 'edge_index'"
        assert hasattr(data, 'y'), "Data must contain labels 'y' for loss computation"
        # More strict feature-dimension checks can be added here if needed.

    def compute_loss(self, data):
        """
        Compute node-wise loss for a graph/subgraph and return a Python list.
        """
        self.check_data_integrity(data)
        data = data.to(self.device)

        with torch.no_grad():
            model_out = self.model(data)
            logits = model_out[0] if isinstance(model_out, (tuple, list)) and len(model_out) > 0 else model_out
            
            # Mask handling
            if hasattr(data, 'train_mask') and data.train_mask is not None:
                mask = data.train_mask.to(self.device)
            else:
                mask = torch.ones(data.y.shape[0], dtype=torch.bool, device=self.device)

            if mask.sum() == 0:
                return []  # return empty list

            targets = data.y.to(self.device).long()
            
            losses = F.cross_entropy(logits[mask], targets[mask], reduction='none')

        # Return as a Python list (via numpy)
        return losses.cpu().numpy().tolist()

    def collect_scores(self, dataset, name="Dataset", verbose: bool = True):
        """
        Collect node-level loss distribution for a dataset.
        """
        all_losses = []
        valid_graphs = 0
        for data in dataset:
            # compute_loss returns a list: [loss_node1, loss_node2, ...]
            node_losses = self.compute_loss(data)
            if len(node_losses) > 0:
                all_losses.extend(node_losses)
                valid_graphs += 1
        
        if verbose:
            print(f"[{name}] Collected {len(all_losses)} valid node samples from {valid_graphs} graphs.")
        return np.array(all_losses)

    def evaluate_unlearning(self, target_data, retained_data, test_data, loss_threshold=None, run_name=None, verbose: bool = True):
        """
        target_data: data to be forgotten (unlearned)
        retained_data: retained/member data
        test_data: test or virtual data (non-members)
        """
        tag = f" [{run_name}]" if run_name else ""
        if verbose:
            print(f"\n--- Running MALT (Loss-Threshold) Evaluation{tag} ---")
        
        # 1) Collect losses
        loss_retained = self.collect_scores(retained_data, "Retained (Member)", verbose=verbose)
        loss_test = self.collect_scores(test_data, "Test/Virtual (Non-Member)", verbose=verbose)
        loss_target = self.collect_scores(target_data, "Target (Unlearned)", verbose=verbose)

        # Guard: cannot compute AUC/threshold with empty samples
        if len(loss_retained) == 0 or len(loss_test) == 0 or len(loss_target) == 0:
            mia_rate = float('nan')
            stats = {
                'mia_rate': mia_rate,
                'auc': float('nan'),
                'mAP': float('nan'),
                'best_accuracy': float('nan'),
                'loss_threshold': float('nan'),
                'target_mean_loss': float(np.mean(loss_target)) if len(loss_target) else float('nan'),
                'retained_mean_loss': float(np.mean(loss_retained)) if len(loss_retained) else float('nan'),
                'virtual_mean_loss': float(np.mean(loss_test)) if len(loss_test) else float('nan'),
                'num_samples_target': int(len(loss_target)),
                'num_samples_retained': int(len(loss_retained)),
                'num_samples_virtual': int(len(loss_test)),
            }
            if verbose:
                print("\n[Attack Performance on Baseline (Retained vs Virtual)]")
                print("  WARNING: Empty loss samples; skipping AUC/threshold.")
                print("\n[Unlearning Results]")
                print(f"  Target Mean Loss: {stats['target_mean_loss']}")
                print(f"  Retained Mean Loss: {stats['retained_mean_loss']}")
                print(f"  MIA Identification Rate on Target: {stats['mia_rate']}")
            return stats
        
        # 2) Fit/evaluate threshold on retained vs virtual
        # Member (Label 1) vs Non-Member (Label 0)
        # Smaller loss => more likely a member. For AUC (higher score => more likely positive), use -loss.
        y_true = np.concatenate([np.ones(len(loss_retained)), np.zeros(len(loss_test))])
        y_scores = np.concatenate([-loss_retained, -loss_test])
        
        # Attack metrics
        # AUC: Area Under Curve
        auc = roc_auc_score(y_true, y_scores)
        # mAP: Mean Average Precision
        ap = average_precision_score(y_true, y_scores)
        
        # If loss_threshold is not provided, search for the threshold maximizing accuracy
        best_acc = float('nan')
        best_thresh_score = float('nan')
        if loss_threshold is None:
            best_acc = 0.0
            best_thresh_score = 0.0
            thresholds = np.unique(y_scores)
            for t in thresholds:
                pred = (y_scores >= t).astype(int)
                acc = accuracy_score(y_true, pred)
                if acc > best_acc:
                    best_acc = acc
                    best_thresh_score = t
            # Convert back to loss threshold (since score = -loss)
            loss_threshold = float(-best_thresh_score)
        else:
            # Use externally provided fixed threshold and report accuracy at that threshold
            loss_threshold = float(loss_threshold)
            pred = (y_scores >= (-loss_threshold)).astype(int)
            best_acc = float(accuracy_score(y_true, pred))
        
        if verbose:
            print(f"\n[Attack Performance on Baseline (Retained vs Virtual)]")
            print(f"  AUC: {auc:.4f} (0.5 = Random, 1.0 = Perfect Separation)")
            print(f"  mAP: {ap:.4f}")
            # best_acc is the accuracy under the chosen threshold (fixed or searched)
            if np.isfinite(best_acc):
                print(f"  Best Accuracy: {best_acc:.4f}")
            else:
                print(f"  Best Accuracy: {best_acc}")
            print(f"  Decision Threshold (Loss): {loss_threshold:.4f}")
            print(f"  (Loss < {loss_threshold:.4f} -> Predicted as Member)")
        
        # 3) Evaluate unlearning: fraction of target samples predicted as members
        # If unlearning succeeds, target losses should look like non-members (larger), reducing member rate.
        is_member_pred = (loss_target < loss_threshold)
        mia_rate = float(np.mean(is_member_pred))
        
        target_mean = float(np.mean(loss_target))
        retained_mean = float(np.mean(loss_retained))
        virtual_mean = float(np.mean(loss_test))
        if verbose:
            print(f"\n[Unlearning Results]")
            print(f"  Target Mean Loss: {target_mean:.4f}")
            print(f"  Retained Mean Loss: {retained_mean:.4f}")
            print(f"  MIA Identification Rate on Target: {mia_rate:.2%}")
            
            if mia_rate < 0.5:
                print("  >> SUCCESS: Model treats Target like Non-Members.")
            else:
                print("  >> WARNING: Model still recognizes Target as Members.")
            
        stats = {
            'mia_rate': mia_rate,
            'auc': float(auc),
            'mAP': float(ap),
            'best_accuracy': float(best_acc),
            'loss_threshold': float(loss_threshold),
            'target_mean_loss': target_mean,
            'retained_mean_loss': retained_mean,
            'virtual_mean_loss': virtual_mean,
            'num_samples_target': int(len(loss_target)),
            'num_samples_retained': int(len(loss_retained)),
            'num_samples_virtual': int(len(loss_test)),
        }
        return stats
